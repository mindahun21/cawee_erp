  KEY `file_access_logs_action_accessed_at_index` (`action`,`accessed_at`),
  KEY `file_access_logs_context_type_context_id_index` (`context_type`,`context_id`),
  KEY `file_access_logs_shared_file_id_foreign` (`shared_file_id`),
  KEY `file_access_logs_file_share_id_foreign` (`file_share_id`),
  KEY `file_access_logs_user_id_foreign` (`user_id`),
  CONSTRAINT `file_access_logs_file_share_id_foreign` FOREIGN KEY (`file_share_id`) REFERENCES `file_shares` (`id`) ON DELETE SET NULL,
  CONSTRAINT `file_access_logs_shared_file_id_foreign` FOREIGN KEY (`shared_file_id`) REFERENCES `shared_files` (`id`) ON DELETE SET NULL,
  CONSTRAINT `file_access_logs_user_id_foreign` FOREIGN KEY (`user_id`) REFERENCES `users` (`id`) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
